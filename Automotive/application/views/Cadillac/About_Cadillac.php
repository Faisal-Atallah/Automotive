            <h3>Founding</h3>

            <p> Cadillac was formed from the remnants of the Henry Ford Company. After a dispute between Henry Ford and his investors, Ford left the company along with several of his key partners in March 1902.
                Ford's financial backers William Murphy and Lemuel Bowen called in engineer Henry M. Leland of Leland & Faulconer Manufacturing Company to appraise the plant and equipment in preparation for liquidating the company's assets.
                Instead, Leland persuaded the pair to continue manufacturing automobiles using Leland's proven single-cylinder engine. A new company called the Cadillac Automobile Company was established on 22 August 1902.
                It was named after French explorer Antoine Laumet de La Mothe, sieur de Cadillac, who founded Detroit in 1701.</p>

