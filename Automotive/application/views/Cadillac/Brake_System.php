                        <li>Worn brake pads</li>
                        <li>Heat-cracked or worn rotors</li>
                        <li>Loose-fitting brake pads in the caliper</li>
                        <li>Missing or damaged noise insulators</li>
                        <li>Uneven torque of lug nuts or caliper hardware</li>
                    </ul>
